/**
 * 
 * @author Thomas LAVOCAT
 * 
 * MetaProperty is not storable in data base a a Searchable object.
 * It define a key value unit. 
 * I know that a list of MetaProperty act more like an hashMap, but I wanted
 * to put them out of MetaData.
 */
public class MetaProperty {
	
	private String name 	= 	"";
	private String value 	=	"";
	
	/**
	 * @param name
	 * @param value
	 */
	public MetaProperty(String name, String value) {
		super();
		this.name = name;
		this.value = value;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}

	/**
	 * @param value the value to set
	 */
	public void setValue(String value) {
		this.value = value;
	}
	
	
}
